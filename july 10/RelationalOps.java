public class RelationalOps {
    public static void main(String[] args) {
        int val1 = 50;
        int val2 = 30;

        System.out.println("val1: " + val1 + ", val2: " + val2);
        System.out.println("Is val1 > val2? " + (val1 > val2));
        System.out.println("Is val1 < val2? " + (val1 < val2));
        System.out.println("Is val1 == val2? " + (val1 == val2));
        System.out.println("Is val1 != val2? " + (val1 != val2));
        System.out.println("Is val1 >= val2? " + (val1 >= val2));
        System.out.println("Is val1 <= val2? " + (val1 <= val2));
    }
}
